# Python code to calculate the area of a rectangle
def calculate_area(length, width):
    return length * width

# Example usage
length = 8
width = 5
area = calculate_area(length, width)
print(f"The area of the rectangle is {area} square centimeters.")

# Python code to calculate probability
def calculate_probability(event_outcomes, total_outcomes):
    return event_outcomes / total_outcomes

# Example usage
event_outcomes = 1
total_outcomes = 6
probability = calculate_probability(event_outcomes, total_outcomes)
print(f"The probability of rolling a 4 on a six-sided die is {probability}.")
