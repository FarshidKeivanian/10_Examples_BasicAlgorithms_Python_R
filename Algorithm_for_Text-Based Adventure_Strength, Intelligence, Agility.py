print("ابتدا، شخصیت خود را بسازید. کیفیت‌های خود را انتخاب کنید.")

# انتخاب کیفیت‌های شخصیت
strength = int(input("قدرت (1-10): "))
intelligence = int(input("هوش (1-10): "))
agility = int(input("چابکی (1-10): "))

print("شما در وسط یک جنگل ایستاده‌اید. دو مسیر پیش روی شماست: یکی به سمت چپ و دیگری به سمت راست.")

choice = input("آیا می‌خواهید به چپ بروید یا به راست؟ (چپ/راست) ")

if choice == "چپ":
    print("شما به سمت چپ رفتید و به یک رودخانه رسیدید.")
    if agility > 5:
        print("با چابکی خود توانستید از رودخانه عبور کنید.")
    else:
        print("متاسفانه در حین عبور از رودخانه غرق شدید. بازی تمام شد.")
elif choice == "راست":
    print("شما به سمت راست رفتید و به یک غار تاریک رسیدید.")
    if intelligence > 5:
        print("با هوش خود توانستید راهی امن از غار پیدا کنید.")
    else:
        print("در تاریکی غار گم شدید. بازی تمام شد.")
else:
    print("انتخاب نامعتبر. لطفا فقط 'چپ' یا 'راست' را وارد کنید.")

print("متشکریم که بازی ما را امتحان کردید!")
